package br.com.diego.controller.result;

public class Teste {

	private String id;
	private String nome;
	
	public Teste() {
	}

	public Teste(String id, String nome) {
		super();
		this.id = id;
		this.nome = nome;
	}

	public String getId() {
		return id;
	}
	public String getNome() {
		return nome;
	}
}
